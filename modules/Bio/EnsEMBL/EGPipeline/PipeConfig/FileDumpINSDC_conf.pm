package Bio::EnsEMBL::EGPipeline::PipeConfig::FileDumpINSDC_conf;

use strict;
use warnings;

use Bio::EnsEMBL::Hive::PipeConfig::HiveGeneric_conf;
use Bio::EnsEMBL::Hive::Version 2.4;

use base ('Bio::EnsEMBL::EGPipeline::PipeConfig::FileDump_conf');

sub default_options {
  my ($self) = @_;
  return {
    %{$self->SUPER::default_options},

    pipeline_name => 'file_dump_insdc_'.$self->o('ensembl_release'),
    
    eg_dir_structure   => 0,
    eg_filename_format => $self->o('eg_dir_structure'),
    out_file_stem      => undef,
    
    insdc_format   => 'embl',
    db_type        => 'core',
    feature_type   => [],
    data_type      => 'features',
    file_type      => $self->o('insdc_format').'.dat',
    gene_centric   => 0,
    per_chromosome => 0,
    source         => 'VectorBase',
    source_url     => 'https://www.vectorbase.org',

    delete_existing => 1,
  };
}

sub pipeline_wide_parameters {
 my ($self) = @_;
 
 return {
   %{$self->SUPER::pipeline_wide_parameters},
   'delete_existing' => $self->o('delete_existing'),
 };
}

sub pipeline_analyses {
  my ($self) = @_;

  return [
    {
      -logic_name        => 'FileDumpINSDC',
      -module            => 'Bio::EnsEMBL::Hive::RunnableDB::Dummy',
      -input_ids         => [ {} ],
      -max_retry_count   => 0,
      -rc_name           => 'normal',
      -flow_into         => [
                              WHEN('#delete_existing#' => ['DeleteExistingFiles'],
                              ELSE ['SpeciesFactory']
                              ),
                            ],
      -meadow_type       => 'LOCAL',
    },

    {
      -logic_name        => 'DeleteExistingFiles',
      -module            => 'Bio::EnsEMBL::Hive::RunnableDB::SystemCmd',
      -max_retry_count   => 0,
      -parameters        => {
                              cmd => 'mkdir -p #results_dir#_tmp;
                                      mv #results_dir#/* #results_dir#_tmp/.',
                            },
      -rc_name           => 'normal',
      -flow_into         => {
                              '1->A' => ['SpeciesFactory'],
                              'A->1' => ['SetFilePermissions'],
                            },
      -meadow_type       => 'LOCAL',
    },

    {
      -logic_name        => 'SpeciesFactory',
      -module            => 'Bio::EnsEMBL::EGPipeline::Common::RunnableDB::EGSpeciesFactory',
      -parameters        => {
                              species         => $self->o('species'),
                              antispecies     => $self->o('antispecies'),
                              division        => $self->o('division'),
                              run_all         => $self->o('run_all'),
                              meta_filters    => $self->o('meta_filters'),
                              chromosome_flow => 0,
                              regulation_flow => 0,
                              variation_flow  => 0,
                            },
      -max_retry_count   => 1,
      -flow_into         => {
                              '2' => ['insdc'],
                            },
      -meadow_type       => 'LOCAL',
    },

    {
      -logic_name        => 'insdc',
	    -module            => 'Bio::EnsEMBL::EGPipeline::FileDump::INSDCDumper',
      -analysis_capacity => 10,
      -max_retry_count   => 1,
      -parameters        => {
                              insdc_format       => $self->o('insdc_format'),
                              db_type            => $self->o('db_type'),
                              feature_type       => $self->o('feature_type'),
                              data_type          => $self->o('data_type'),
                              file_type          => $self->o('file_type'),
                              gene_centric       => $self->o('gene_centric'),
                              per_chromosome     => $self->o('per_chromosome'),
                              source             => $self->o('source'),
                              source_url         => $self->o('source_url'),
                              out_file_stem      => $self->o('out_file_stem'),
                              eg_dir_structure   => $self->o('eg_dir_structure'),
                              eg_filename_format => $self->o('eg_filename_format'),
                              taxonomy_db        => $self->o('taxonomy_db'),
                            },
      -rc_name           => 'normal',
      -flow_into         => ['CompressFile'],
	  },

    {
      -logic_name        => 'CompressFile',
      -module            => 'Bio::EnsEMBL::Hive::RunnableDB::SystemCmd',
      -analysis_capacity => 10,
      -batch_size        => 10,
      -max_retry_count   => 0,
      -parameters        => {
                              cmd => 'gzip -n -f #out_file#',
                            },
      -rc_name           => 'normal',
    },

    {
      -logic_name        => 'SetFilePermissions',
      -module            => 'Bio::EnsEMBL::Hive::RunnableDB::SystemCmd',
      -max_retry_count   => 0,
      -parameters        => {
                              cmd => 'chmod g+rw #results_dir#/*',
                            },
      -rc_name           => 'normal',
    },

  ];
}

1;
